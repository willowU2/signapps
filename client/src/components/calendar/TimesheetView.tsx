type TimesheetStatus = "pending" | "validated" | "exported";

interface TimesheetCategory {
  id: string;
  name: string;
  color?: string;
}

interface DayEntry {
  hours: number;
  autoGenerated: boolean;
  modified: boolean;
  entryId?: string;
}

interface CategoryRow {
  category: TimesheetCategory;
  dayEntries: Record<string, DayEntry>; // ISO date (YYYY-MM-DD) -> entry
}

interface TimesheetData {
  id?: string;
  week: string; // e.g. "2026-W13"
  status: TimesheetStatus;
  generatedAt?: string;
  validatedAt?: string;
  exportedAt?: string;
  rows: CategoryRow[];
}

