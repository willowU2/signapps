const DAYS_OF_WEEK = ["Lun", "Mar", "Mer", "Jeu", "Ven", "Sam", "Dim"];

function getWeekKey(date: Date): string {
  const year = getYear(startOfWeek(date, { weekStartsOn: 1 }));
  const week = getISOWeek(date);
  return `${year}-W${String(week).padStart(2, "0")}`;
}

function getWeekDays(date: Date): Date[] {
  const start = startOfWeek(date, { weekStartsOn: 1 });
  return Array.from({ length: 7 }, (_, i) => addDays(start, i));
}

function formatHours(h: number): string {
  if (h === 0) return "—";
  return h % 1 === 0 ? `${h}h` : `${h.toFixed(1)}h`;
}

function sumRow(row: CategoryRow, days: Date[]): number {
  return days.reduce((acc, d) => {
    const key = format(d, "yyyy-MM-dd");
    return acc + (row.dayEntries[key]?.hours ?? 0);
  }, 0);
}

function sumDay(rows: CategoryRow[], dateKey: string): number {
  return rows.reduce(
    (acc, row) => acc + (row.dayEntries[dateKey]?.hours ?? 0),
    0
  );
}

function sumTotal(rows: CategoryRow[], days: Date[]): number {
  return days.reduce((acc, d) => {
    const key = format(d, "yyyy-MM-dd");
    return acc + sumDay(rows, key);
  }, 0);
}

const DEFAULT_CATEGORIES: TimesheetCategory[] = [
  { id: "normal", name: "Heures normales", color: "#3b82f6" },
  { id: "formation", name: "Formation", color: "#8b5cf6" },
  { id: "astreinte", name: "Astreinte", color: "#f59e0b" },
  { id: "deplacement", name: "Déplacement", color: "#10b981" },
  { id: "reunion", name: "Réunion", color: "#ec4899" },
];

function buildEmptyRow(
  category: TimesheetCategory,
  days: Date[]
): CategoryRow {
  const dayEntries: Record<string, DayEntry> = {};
  days.forEach((d) => {
    dayEntries[format(d, "yyyy-MM-dd")] = {
      hours: 0,
      autoGenerated: false,
      modified: false,
    };
  });
  return { category, dayEntries };
}

// Merge API data into rows structure
function mergeApiEntries(
  rows: CategoryRow[],
  apiEntries: any[],
  days: Date[]
): CategoryRow[] {
  const merged = rows.map((row) => ({
    ...row,
    dayEntries: { ...row.dayEntries },
  }));

  apiEntries.forEach((entry: any) => {
    const dateKey = entry.date
      ? format(parseISO(entry.date), "yyyy-MM-dd")
      : null;
    if (!dateKey) return;
    const rowIdx = merged.findIndex(
      (r) =>
        r.category.id === entry.category_id ||
        r.category.name === entry.category
    );
    if (rowIdx >= 0) {
      merged[rowIdx].dayEntries[dateKey] = {
        hours: entry.hours ?? 0,
        autoGenerated: entry.auto_generated ?? false,
        modified: false,
        entryId: entry.id,
      };
    }
  });

  return merged;
}

