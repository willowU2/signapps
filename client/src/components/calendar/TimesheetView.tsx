interface EditableCellProps {
  value: number;
  autoGenerated: boolean;
  modified: boolean;
  isToday: boolean;
  onChange: (val: number) => void;
  disabled: boolean;
}

function EditableCell({
  value,
  autoGenerated,
  modified,
  isToday: isTodayDay,
  onChange,
  disabled,
}: EditableCellProps) {
  const [editing, setEditing] = useState(false);
  const [raw, setRaw] = useState("");
  const inputRef = useRef<HTMLInputElement>(null);

  const handleDoubleClick = () => {
    if (disabled) return;
    setRaw(value === 0 ? "" : String(value));
    setEditing(true);
  };

  useEffect(() => {
    if (editing && inputRef.current) {
      inputRef.current.focus();
      inputRef.current.select();
    }
  }, [editing]);

  const commit = () => {
    const parsed = parseFloat(raw.replace(",", "."));
    const next = isNaN(parsed) ? 0 : Math.max(0, Math.min(24, parsed));
    onChange(next);
    setEditing(false);
  };

  const handleKeyDown = (e: React.KeyboardEvent) => {
    if (e.key === "Enter" || e.key === "Tab") commit();
    if (e.key === "Escape") setEditing(false);
  };

  if (editing) {
    return (
      <td className="px-1 py-1 text-center w-[80px]">
        <Input
          ref={inputRef}
          value={raw}
          onChange={(e) => setRaw(e.target.value)}
          onBlur={commit}
          onKeyDown={handleKeyDown}
          className="h-7 w-16 text-center text-sm mx-auto"
          placeholder="0"
        />
      </td>
    );
  }

  return (
    <td
      onDoubleClick={handleDoubleClick}
      className={cn(
        "px-2 py-2 text-center text-sm w-[80px] select-none transition-colors",
        "group cursor-default",
        !disabled && "hover:bg-muted/40 hover:cursor-pointer",
        isTodayDay && "bg-primary/5",
        modified && "bg-yellow-500/10 dark:bg-yellow-400/10",
        value > 0 && !modified && "text-foreground",
        value === 0 && "text-muted-foreground/50"
      )}
      title={disabled ? undefined : "Double-clic pour modifier"}
    >
      <span className="flex items-center justify-center gap-1">
        {autoGenerated && value > 0 && (
          <TooltipProvider>
            <Tooltip>
              <TooltipTrigger asChild>
                <Bot className="h-3 w-3 text-primary/60 shrink-0" />
              </TooltipTrigger>
              <TooltipContent side="top">
                <p>Généré automatiquement</p>
              </TooltipContent>
            </Tooltip>
          </TooltipProvider>
        )}
        {value === 0 ? "—" : formatHours(value)}
      </span>
    </td>
  );
}

// ============================================================================
// Status Badge
// ============================================================================

function StatusBadge({ status }: { status: TimesheetStatus }) {
  if (status === "validated") {
    return (
      <Badge className="gap-1 bg-green-500/15 text-green-700 dark:text-green-400 border-green-500/30 hover:bg-green-500/20">
        <CheckCircle className="h-3 w-3" />
        Validé
      </Badge>
    );
  }
  if (status === "exported") {
    return (
      <Badge className="gap-1 bg-blue-500/15 text-blue-700 dark:text-blue-400 border-blue-500/30 hover:bg-blue-500/20">
        <Download className="h-3 w-3" />
        Exporté
      </Badge>
    );
  }
  return (
    <Badge className="gap-1 bg-orange-500/15 text-orange-700 dark:text-orange-400 border-orange-500/30 hover:bg-orange-500/20">
      <Clock className="h-3 w-3" />
      Non validé
    </Badge>
  );
}

